    public void insert(String gender, double amt, double dscnt_amt, String gst_8, String gst_12, String gst_18, String gst_28, double net_gst, double net_amt)
    {
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/java_projects","root","");
            Statement st = conn.createStatement();
            int m = st.executeUpdate("insert into my_mall values ('"+gender+"',"+amt+","+dscnt_amt+",'"+gst_8+"','"+gst_12+"','"+gst_18+"','"+gst_28+"',"+net_gst+","+net_amt+")");
            st.close();
            conn.close();
        }
        catch(Exception e)
        {
            System.out.println(e);
        }
    }
    
