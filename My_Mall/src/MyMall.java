    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
        jTextField6.setText("");
        jTextField7.setText("");
        jTextField8.setText("");
        gst_8 = "No";
        gst_12 = "No";
        gst_18 = "No";
        gst_28 = "No";
        gender = "";
        net_gst = 0;
        if(jRadioButton1.isSelected())
        {
            dscnt = 0.05;
            gender = "Male";
        }
        if(jRadioButton2.isSelected())
        {
            dscnt = 0.20;
            gender = "Female";
        }
        amt = Integer.parseInt(jTextField1.getText());
        dscnt_amt = amt - (amt * dscnt);
        jTextField2.setText("Rs." + dscnt_amt);
        if(dscnt_amt <= 2000)
        {
            gst = 0;
            net_gst = dscnt_amt * gst;
            jTextField7.setText("No GST is applied...");
        }
        else if(dscnt_amt <= 5000)
        {
            gst = 0.08;
            net_gst = dscnt_amt * gst;
            gst_8 = "Yes";
            jTextField3.setText("" + (net_gst));
            jTextField7.setText("GST @ 8% is applied...");
        }
        else if(dscnt_amt <= 10000)
        {
            gst = 0.12;
            net_gst = dscnt_amt * gst;
            gst_12 = "Yes";
            jTextField4.setText("" + (net_gst));
            jTextField7.setText("GST @ 12% is applied...");
        }
        else if(dscnt_amt <= 20000)
        {
            gst = 0.18;
            net_gst = dscnt_amt * gst;
            gst_18 = "Yes";
            jTextField5.setText("" + (net_gst));
            jTextField7.setText("GST @ 18% is applied...");
        }
        else
        {
            gst = 0.28;
            net_gst = dscnt_amt * gst;
            gst_28 = "Yes";
            jTextField6.setText("" + (net_gst));
            jTextField7.setText("GST @ 28% is applied...");
        }
        net_amt = dscnt_amt + net_gst;
        jTextField8.setText("Rs." + net_amt);
        insert(gender,amt,dscnt_amt,gst_8,gst_12,gst_18,gst_28,net_gst,net_amt);
    }//GEN-LAST:event_jButton1ActionPerformed

