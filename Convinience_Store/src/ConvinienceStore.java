    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ppu = Integer.parseInt(jTextField3.getText());
        qty = Integer.parseInt(jTextField4.getText());
        sls_prc = ppu * qty;
        jTextField5.setText("Rs." + sls_prc);
        if(jRadioButton1.isSelected())
        {
            srvc_chrgs=(int)(sls_prc*0.10);
            clss = 1;
        }
        if (jRadioButton2.isSelected())
        {
            srvc_chrgs=(int)(sls_prc*0.15);
            clss = 2;
        }
        if (jRadioButton3.isSelected())
        {
            srvc_chrgs=(int)(sls_prc*0.20);
            clss = 3;
        }
        jTextField6.setText("Rs."+srvc_chrgs);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        name = jTextField1.getText();
        itm = jTextField2.getText();
        net_prc = sls_prc + srvc_chrgs;
        jTextField7.setText("Rs." + net_prc);
        jTextArea1.setText("Thank you for shopping with us Mr./Ms./Mrs. " + name + " .Please pay Rs." + net_prc + " for the item " + itm);
        insert(name,itm,ppu,qty,clss,sls_prc,srvc_chrgs,net_prc);
    }//GEN-LAST:event_jButton2ActionPerformed
    
    public void insert(String name,String itm,int ppu,int qty,int clss,int sls_prc,int srvc_chrgs,int net_prc)
    {
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/java_projects","root","");
            Statement st = conn.createStatement();
            int m = st.executeUpdate("insert into convenience_store values ('"+name+"','"+itm+"',"+ppu+","+qty+","+clss+","+sls_prc+","+srvc_chrgs+","+net_prc+")");
            if(m == 1)
            {
                System.out.println("Done...");
            }
            st.close();
            conn.close();
        }
        catch(Exception e)
        {
            System.out.println(e);
        }
    }
    
