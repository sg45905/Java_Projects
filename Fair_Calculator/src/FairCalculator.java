    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        fest = false;
        name = jTextField1.getText();
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
        dist = Integer.parseInt(jTextField2.getText());
        md = jList1.getSelectedIndex();
        switch(md)
        {
            case 0:
                ppkm = 10;
                break;
            case 1:
                ppkm = 15;
                break;
            case 2:
                ppkm = 40;
                break;
        }
        amt = dist * ppkm;
        jTextField3.setText("Rs." + amt);
        if(jRadioButton1.isSelected())
        {
            g_dscnt = 0.0;
            gender = "male";
        }
        if(jRadioButton2.isSelected())
        {
            g_dscnt = 0.25;
            gender = "female";
        }
        if(jCheckBox1.isSelected())
        {
            f_dscnt = 0.05;
            fest = true;
        }
        ge_dscnt = amt * g_dscnt;
        imd_fr = (int)(amt - ge_dscnt);
        fe_dscnt = imd_fr * f_dscnt;
        net_amt = (int)(imd_fr - fe_dscnt);
        dscnt = (int) (ge_dscnt + fe_dscnt);
        jTextField4.setText("Rs." + dscnt);
        jTextField5.setText("Rs." + net_amt);
        insert(name,dist,ppkm,gender,fest,amt,dscnt,net_amt);
    }//GEN-LAST:event_jButton1ActionPerformed
    
    public void insert(String name,int dist,int ppkm,String gender,boolean fest,int amt,int dscnt,int net_amt)
    {
        String fe = "No";
        if(fest)
        {
            fe = "Yes";
        }
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/java_projects","root","");
            Statement st = conn.createStatement();
            int m = st.executeUpdate("insert into fair_calculator values ('"+name+"',"+dist+","+ppkm+",'"+gender+"','"+fe+"',"+amt+","+dscnt+","+net_amt+")");
            st.close();
            conn.close();
        }
        catch(Exception e)
        {
            System.out.println(e);
        }
    }
    
