    public void insert(String name,String painting,String essay_writing,String movie_making,int ttl_pts)
    {
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/java_projects","root","");
            Statement st = conn.createStatement();
            int m = st.executeUpdate("insert into point_calculator values ('"+name+"','"+painting+"','"+essay_writing+"','"+movie_making+"',"+ttl_pts+")");
            st.close();
            conn.close();
        }
        catch(Exception e)
        {
            System.out.println(e);
        }
    }
    
