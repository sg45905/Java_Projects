    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        adns = 0;
        adons = "";
        fest = false;
        if(jCheckBox1.isSelected())
        {
            adns += 799;
            adons += "Sweet Box";
        }
        if(jCheckBox2.isSelected())
        {
            adns += 500;
            adons += "Extra Chocolate";
        }
        if(jRadioButton1.isSelected())
        {
            prc = 900;
            combo = 1;
        }
        if(jRadioButton2.isSelected())
        {
            prc = 1100;
            combo = 2;
        }
        amt = prc + adns;
        if(jRadioButton3.isSelected())
        {
            dscnt = 0.15;
            fest = true;
        }
        dscnt_amt = (int) (amt * dscnt);
        jTextField3.setText("Rs." + dscnt_amt);
        net_amt = amt - dscnt_amt;
        jTextField4.setText("Rs." + net_amt);
        name = jTextField1.getText();
        city = jTextField2.getText();
        insert(name,city,adons,fest,combo,dscnt_amt,net_amt);
    }//GEN-LAST:event_jButton1ActionPerformed
    
    public void insert(String name,String city,String adons,boolean fest,int combo,int dscnt,int net_amt)
    {
        String fe = "No";
        if(fest)
        {
            fe = "Yes";
        }
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/java_projects","root","");
            Statement st = conn.createStatement();
            int m = st.executeUpdate("insert into sweet_shop values ('"+name+"','"+city+"','"+adons+"','"+fe+"',"+combo+","+dscnt+","+net_amt+")");
            st.close();
            conn.close();
        }
        catch(Exception e)
        {
            System.out.println(e);
        }
    }
    
